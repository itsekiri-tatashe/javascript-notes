// For Loop
for (let index = 1; index <= 5; index++) {
    console.log("Iteration number " + index)
}


// While Loop
let index = 1
while (index <= 5) {
    console.log("Iteration number " + index)
    index++
}


// Do while
index = 1
do {
    console.log("Iteration number " + index)
    index++
} while (index <= 5);


// For of Loop
const nameArray = ["Eyimofe", "Jola", "Deborah", "Wenyinmi", "Pinnick"]
for (const name of nameArray) {
    console.log(name)
}